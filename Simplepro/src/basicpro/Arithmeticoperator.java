package basicpro;

public class Arithmeticoperator {
	public static void main(String[]args) {
		int x=30;
		int y=50;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x/y);
		System.out.println(x*y);
		
		int a=30;
		a++;
		System.out.println(a);
		int b=20;
		b--;
		System.out.println(b);
	
	}

}
