package basicpro;

public class Assignmentoperator {
	public static void main(String[]args){
		int a=40;
		a+=20;
		System.out.println(a);
		int b=50;
		b-=30;
		System.out.println(b);
		int c=20;
		c/=10;
		System.out.println(c);
		int d=60;
		d*=20;
		System.out.println(d);
		int x=80;
		x%=30;
		System.out.println(x);
	}

}
